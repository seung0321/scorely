function parseAndValidate(raw: string): AnalysisResult {
  const cleaned = raw
    .replace(/```json\s*/gi, '')
    .replace(/```\s*/g, '')
    .trim()

  const parsed: RawAnalysis = JSON.parse(cleaned) as RawAnalysis

  const scores = parsed.scores
  const scoreKeys: (keyof ScoreDetail)[] = ['expertise', 'experience', 'achievement', 'communication', 'structure']

  for (const key of scoreKeys) {
    const score = scores[key]
    if (typeof score !== 'number' || score < 0 || score > 100) {
      throw new Error('점수 범위 오류')
    }
    // scoreCalculation 합계와 scores 일치 강제 교정
    const calcEntry = parsed.scoreCalculation?.[key]
    if (calcEntry) {
      const calcTotal = typeof calcEntry['합계'] === 'number'
        ? calcEntry['합계']
        : Object.entries(calcEntry).filter(([k]) => k !== '합계').reduce((sum, [, v]) => sum + (typeof v === 'number' ? v : 0), 0)
      if (Math.abs(calcTotal - score) > 2) {
        // scoreCalculation이 실제 계산 근거이므로 이 값으로 강제 교정 (0~100 범위 캡)
        console.warn(`[점수 불일치 교정] ${key}: calculation=${calcTotal}, score=${score} → ${Math.max(0, Math.min(100, calcTotal))}으로 교정`)
        scores[key] = Math.max(0, Math.min(100, calcTotal))
      }
    }
  }

  const penalties = Array.isArray(parsed.penalties) ? parsed.penalties : []
  // 개선사항 최대 3개 강제 제한
  const improvements = Array.isArray(parsed.improvements) ? parsed.improvements.slice(0, 3) : []

  return {
    scores,
    totalScore: calcTotalScore(scores),
    strengths: parsed.strengths,
    improvements,
    penalties,
    oneLiner: parsed.oneLiner,
  }
}

export async function analyzeResume(
  resumeText: string,
  jobCategory: JobCategory,
  experienceLevel: ExperienceLevel,
  sections?: ResumeSections,
): Promise<AnalysisResult> {
  const prompt = buildPrompt(jobCategory, resumeText, experienceLevel, sections)
  const RETRY_HINT = '\n\n이전 응답이 JSON 파싱에 실패했습니다. 반드시 순수 JSON만 반환하세요. 마크다운, 코드블록, 설명 텍스트 없이 JSON만 출력하세요.'

  try {
    const result = await ai.models.generateContent({
      model: MODEL,
      contents: prompt,
    })
    const raw = result.text ?? ''

    try {
      return parseAndValidate(raw)
    } catch {
      // 1회 재시도 (JSON 파싱 실패 힌트 포함)
      const retryResult = await ai.models.generateContent({
        model: MODEL,
        contents: prompt + RETRY_HINT,
      })
      const retryRaw = retryResult.text ?? ''
      try {
        return parseAndValidate(retryRaw)
      } catch {
        throw new AppError(500, 'AI 응답 파싱에 실패했습니다', 'AI_ERROR')
      }
    }
  } catch (err) {
    if (err instanceof AppError) throw err
    throw new AppError(500, 'AI 분석 중 오류가 발생했습니다', 'AI_ERROR')
  }
}

